export class Tecnico {
    /**
     * Creates an instance of Tecnico.
     * @param id
     * @param name
     * @param lastname
     * @param speciality
     * @param description
     */
    constructor(id, name, lastname, speciality, description) {
        this.id = id;
        this.name = name;
        this.lastname = lastname;
        this.speciality = speciality;
        this.description = description;
    }
}